public class Fahrenheit2Celsius {
    public static void main(String[] args){

        double Fahrenheit;
        double Celsius;

        Fahrenheit = 68.0;

        Celsius = (Fahrenheit-32)/1.8;

        System.out.println("화씨 온도 "+ Fahrenheit + "은 " + Celsius + "입니다.");


    }
}
